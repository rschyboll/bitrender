from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware

from storage import init_db
from api import tasks

app = FastAPI()

origins = [
    "http://127.0.0.1:3000",
    "http://localhost:3000",
]

app.add_middleware(
    CORSMiddleware,
    allow_origins=origins,
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

app.include_router(tasks.router)

init_db(app)
